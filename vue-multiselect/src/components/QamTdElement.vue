<!--======================================================================
//========================================================================
// Template: QamTdElement
//========================================================================
//========================================================================-->
<template>
  <td v-if="isCheckboxField()" :class="generateClassName()" class="qam-td"><input type="checkbox" @click="storeTable.toggleCheckbox()" class="qam-checkbox" /></td>
  <td v-else :class="generateClassName()" class="qam-td">{{ props.cell }}</td>
</template>

<!--======================================================================
//========================================================================
// Script
//========================================================================
//========================================================================-->
<script setup>
//========================================================================
// Imports
//========================================================================
import { onMounted } from "vue";
import { useStoreTable } from "@/stores/storeTable";

//========================================================================
// Properties
//========================================================================
const props = defineProps({
  // Cell content
  cell: { type: String },
  // Cell index (column)
  index: { type: Number },
});

//========================================================================
// Store
//========================================================================
const storeTable = useStoreTable();

//========================================================================
// Reactive Variables
//========================================================================

//========================================================================
// Emits
//========================================================================

//========================================================================
// Computed
//========================================================================

//========================================================================
// Lifecycle Hooks
//========================================================================
onMounted(() => {});

//========================================================================
// Methods
//========================================================================

// Determine if the field is a checkbox field from field property.
const isCheckboxField = () => {
  if (props.cell == "Checkbox") {
    return true;
  }
  return false;
};

// Generate the th class name by adding column index.
const generateClassName = () => {
  return "qam-td-" + props.index;
};
</script>

